using MagDbPatcher.Services;
using Xunit;

namespace MagDbPatcher.Tests;

public class PatcherConfigTests
{
    [Fact]
    public async Task StableToBuildSameMajor_GeneratesPatchesForAnyMajor()
    {
        var root = CreateTempDir();
        try
        {
            var patches = Path.Combine(root, "patches");
            Directory.CreateDirectory(patches);

            await File.WriteAllTextAsync(Path.Combine(patches, "versions.json"), """{ "versions": [], "patches": [] }""");
            await File.WriteAllTextAsync(Path.Combine(patches, "patcher.config.json"), """
            {
              "schemaVersion": 1,
              "versionOrdering": { "mode": "semantic_with_optional_buildDate" },
              "autoGenerate": {
                "buildVersionPattern": "-",
                "rules": [ { "type": "stable_to_build_same_major" } ]
              }
            }
            """);

            Directory.CreateDirectory(Path.Combine(patches, "8.0"));
            Directory.CreateDirectory(Path.Combine(patches, "8.1.0-20260201"));
            await File.WriteAllTextAsync(Path.Combine(patches, "8.1.0-20260201", "a.sql"), "SELECT 1;");

            var svc = new VersionService(patches);
            await svc.LoadVersionsAsync();

            Assert.False(svc.LastValidationResult.HasErrors);

            var patch = svc.GetAllPatches().FirstOrDefault(p => p.From == "8.0" && p.To == "8.1.0-20260201");
            Assert.NotNull(patch);
            Assert.True(patch!.AutoGenerated);
            Assert.Contains("8.1.0-20260201/a.sql", patch.Scripts);
        }
        finally
        {
            TryDelete(root);
        }
    }

