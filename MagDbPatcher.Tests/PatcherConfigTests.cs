    [Fact]
    public async Task VersionOrdering_SortsStableBeforeBuildForSameSemver()
    {
        var root = CreateTempDir();
        try
        {
            var patches = Path.Combine(root, "patches");
            Directory.CreateDirectory(patches);

            await File.WriteAllTextAsync(Path.Combine(patches, "versions.json"), """{ "versions": [], "patches": [] }""");
            await File.WriteAllTextAsync(Path.Combine(patches, "patcher.config.json"), """
            {
              "schemaVersion": 1,
              "versionOrdering": { "mode": "semantic_with_optional_buildDate" },
              "autoGenerate": {
                "buildVersionPattern": "-",
                "rules": [ { "type": "stable_to_build_same_major" } ]
              }
            }
            """);

            Directory.CreateDirectory(Path.Combine(patches, "7.2.3"));
            Directory.CreateDirectory(Path.Combine(patches, "7.2.3-20260123"));
            await File.WriteAllTextAsync(Path.Combine(patches, "7.2.3-20260123", "a.sql"), "SELECT 1;");

            var svc = new VersionService(patches);
            await svc.LoadVersionsAsync();

            var versions = svc.GetAllVersions().Select(v => v.Id).ToList();
            var idxStable = versions.IndexOf("7.2.3");
            var idxBuild = versions.IndexOf("7.2.3-20260123");

            Assert.True(idxStable >= 0);
            Assert.True(idxBuild >= 0);
            Assert.True(idxStable < idxBuild);

            var patch = svc.GetAllPatches().FirstOrDefault(p => p.From == "7.2.3" && p.To == "7.2.3-20260123");
            Assert.NotNull(patch);
            Assert.True(patch!.AutoGenerated);
        }
        finally
        {
            TryDelete(root);
        }
    }

    private static string CreateTempDir()
    {
        var dir = Path.Combine(Path.GetTempPath(), "MagDbPatcherTests_" + Guid.NewGuid().ToString("N"));
        Directory.CreateDirectory(dir);
        return dir;
    }

    private static void TryDelete(string path)
    {
        try
        {
            if (Directory.Exists(path))
                Directory.Delete(path, recursive: true);
        }
        catch
        {
        }
    }
}

